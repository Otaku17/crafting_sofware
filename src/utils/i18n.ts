const MAP: Record<Lang, Translations> = { en: EN, fr: FR };

export function t(lang: Lang, key: string, vars?: Record<string, string>): string {
  let s = MAP[lang][key] ?? MAP.en[key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      s = s.replace(`{${k}}`, v);
    }
  }
  return s;
}
